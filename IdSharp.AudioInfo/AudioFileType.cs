        /// Ogg-Vorbis (*.ogg)
        /// </summary>
        OggVorbis,
        /// <summary>
        /// OptimFROG (*.ofr)
        /// </summary>
        OptimFrog,
        /// <summary>
        /// RIFF-WAVE (*.wav)
        /// </summary>
        RiffWave,
        /// <summary>
        /// Shorten (*.shn)
        /// </summary>
        Shorten,
        /// <summary>
        /// Windows Media Audio (*.wma)
        /// </summary>
        WindowsMedia,
        /// <summary>
        /// WavPack (*.wv)
        /// </summary>
        WavPack,
        /// <summary>
        /// True Audio (*.tta)
        /// </summary>
        TrueAudio
    }
}
